class Result{

	public static void main(String[] args){
	
		int mark = Integer.parseInt(args[0]);
		
		
		
	if(mark>70){
		
					System.out.println("Grade is : FCD");
					
				}
					
	if(70>mark && mark>60){
		
					System.out.println("Grade is : FC");
					
					}
					
					
	if(60>mark && mark>50){
		
					System.out.println("Grade is : SC");
					
					}
					
	if(50>mark && mark>35){
		
					System.out.println("Grade is : Pass");
					
					}
					
					
	if(35>mark){
		
					System.out.println("Grade is : Fail");
					
					}
		
		
		
		
	
	
	
	}


}